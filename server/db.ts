import { Pool } from 'pg'; // ‚úÖ correct ESM import
import { drizzle } from 'drizzle-orm/node-postgres';
import * as schema from '@shared/schema';

let pool, db;

try {
  if (!process.env.DATABASE_URL) {
    console.error("‚ùå DATABASE_URL must be set. Did you forget to provision a database?");
    process.exit(1);
  }

  pool = new Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: {
      rejectUnauthorized: false, // ‚úÖ required for Render PostgreSQL
    },
  });

  db = drizzle(pool, { schema });
  console.log("‚úÖ Database connected successfully");

} catch (err) {
  console.error("üö® Failed to initialize database:", err);
  process.exit(1);
}

export { pool, db };
